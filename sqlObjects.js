function Tweet(id, user, topic, tweet) {
  this.id = id;
  this.user = user;
  this.topic = topic;
  this.tweet = tweet;
}

function Followers(id, user, followers) {
  this.id = id;
  this.user = user;
  this.followers = followers;
}

function Friends(id, user, friends) {
  this.id = id;
  this.user - user;
  this.friends = friends;
}

function Ids(id, user, user_id) {
  this.id = id;
  this.user = user;
  this.user_id = user_id;
}
